public class RaceHorse extends Horse 
{
	 
	 String nameRace;
	 String colorRace;
	private int yearRace;
	private  int numRace; 
	
	
	
	
	   public String getNameRace() 
	   {
	       return nameRace;
	   }
	   public void setNameRace(String nameRace) 
	   {
	       this.nameRace = nameRace;
	   }
	   
	   public String getColorRace() 
	   {
	       return colorRace;
	   }
	   public void setColorRace(String colorRace) 
	   {
	       this.colorRace = colorRace;
	   }
	   
	   
	   public int getYearRace() {
			return yearRace;
		}
		public void setYearRace(int yearRace) {
			this.yearRace = yearRace;
		}
	   
	   
	   public int getNumRace() 
	   {
	       return numRace;
	   }
	   public void setNumRace(int numRace) 
	   {
	       this.numRace = numRace;
	   }
	   
}

